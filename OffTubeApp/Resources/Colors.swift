import UIKit

extension UIColor {
    /// Fundo escuro para o app
    static var darkBackground: UIColor {
        return UIColor(white: 0.1, alpha: 1.0)
    }
    
    /// Um azul neon fluorescente (ajustado para um tom de azul verdadeiro)
    static var neonBlue: UIColor {
        return UIColor(red: 0.0, green: 0.67, blue: 1.0, alpha: 1.0)
    }
}

